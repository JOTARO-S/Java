public class Operator {
    public static void main(String[] args) {
        
        int a = 15;
        System.out.println("a = " + a);
        int c;
        c = a + 3;
        System.out.println("c = " + c);
        c = a - 5;
        System.out.println("c = " + c);
        c = a * 2;
        System.out.println("c = " + c);
        c = a / 6;
        System.out.println("c = " + c);
        c = a % 4;
        System.out.println("c = " + c);
        
        a = 10;
        a++;
        System.out.println("a = " + a);
        
        a = 10;
        a--;
        System.out.println("a = " + a);
        
        String x = "100";
        String y = "200";
        
        String z = x + y;
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
    }
}

    